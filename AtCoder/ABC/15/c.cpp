int choice[6];
bool ok = true;

void dfs(int len = 0) {
  if (len == N) {
    ll now = 0;
    rep(i, N) { now ^= choice[i]; }
    if (now == 0)
      ok = false;
    return;
  }
  rep(i, K) {
    choice[len] = A[len][i];
    dfs(len + 1);
  }
}

