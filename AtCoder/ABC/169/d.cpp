map< ll, int > prime_factor(ll n) {
  map< ll, int > ret;
  for(int64_t i = 2; i * i <= n; i++) {
    while(n % i == 0) {
      ret[i]++;
      n /= i;
    }
  }
  if(n != 1) ret[n] = 1;
  return ret;
}

int main() {
  ll n; cin >> n;
  auto primes = prime_factor(n);
  ll ans = 0;
  for (auto p: primes) {
    int x = p.second;
    int dif = 1;
    while (x - dif >= 0) {
      x -= dif;
      dif++;
      ans++;
    }
  }
  cout << ans << endl;
  return 0;
}