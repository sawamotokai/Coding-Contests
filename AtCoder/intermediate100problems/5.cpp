const ll LINF = 1e18L + 1;
const int INF = 1e9 + 1;
//clang++ -std=c++11 -stdlib=libc++ 

int A,B,C,X,Y;  
int main() {
  cin >> A >> B >> C>>X>>Y;
  ll ans = LINF;
  for (int ab = 0; ab <= 2*max(X,Y); ab+=2) {
    int need_a = max(0, X-ab/2);
    int need_b = max(0, Y-ab/2);
    ll cost = need_a * A + need_b * B + ab * C;
    chmin(ans, cost);
  }
  printf("%lld\n", ans);
  return 0;
}